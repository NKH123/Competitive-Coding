map <ll, int> dp;
int DP(ll n) {
    if (n < 0) return 0;
    if (n == 0) return 1;
    if (dp.count(n)) return dp[n];
    int res = 0;
    if (n & 1) res = (DP(n / 2) * 2 % mod + DP(n / 2 - 1)) % mod;
    else res = (DP(n / 2) + DP(n / 2 - 1) * 2 % mod) % mod;
    return dp[n] = res;
}

